package Polymorphism;

public class SumOperations {

    public void sum(int a, int b){
        System.out.println("Sum of two digits are: " + (a + b));
    }

    public void sum(int a, int b, int c){
        System.out.println("Sum of three digits are: " + (a + b + c));
    }

    public void sum(float a, float b){
        System.out.println("Sum of two float digits are: " + (a + b));
    }
}
